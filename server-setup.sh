#!/bin/bash

# Server Setup Script for Ubuntu
echo "üîß –ü–æ–¥–≥–æ—Ç–æ–≤–∫–∞ Ubuntu —Å–µ—Ä–≤–µ—Ä–∞ –¥–ª—è DevAssist Pro"
